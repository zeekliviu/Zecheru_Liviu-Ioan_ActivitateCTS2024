package e.ex10.clase;

public class Dimensiune {
    private float inaltime;
    private float latime;

    public Dimensiune(float inaltime, float latime) {
        this.inaltime = inaltime;
        this.latime = latime;
    }

    public float getInaltime() {
        return inaltime;
    }

    public float getLatime() {
        return latime;
    }
}
