package facade.autobuz;

public class Autobuz {
    public void punePeLiberUsaDinFata()
    {
        System.out.println("Ușa din față eliberată!");
    }
    public void punePeLiberUsaDinMijloc()
    {
        System.out.println("Ușa din mijloc eliberată!");
    }
    public void punePeLiberUsaDinSpate()
    {
        System.out.println("Ușa din spate eliberată!");
    }
    public void deschideUsaDinFata()
    {
        System.out.println("Ușa din față deschisă!");
    }
    public void deschideUsaDinMijloc()
    {
        System.out.println("Ușa din mijloc deschisă!");
    }
    public void deschideUsaDinSpate()
    {
        System.out.println("Ușa din spate deschisă!");
    }
}
